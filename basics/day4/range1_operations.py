## Range operations

print(range(5))
print("----------------------------------")

for v in range(5):
    print(v)
print("----------------------------------")

ls = list(range(5))
print(ls)

print("----------------------------------")
for v in range(1,5):
    print(v)

print("----------------------------------")
for v in range(2,10,2): # 2 is starting value, 10 is limit value, 2 is range 
    print(v)
